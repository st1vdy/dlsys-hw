class ReLU(TensorOp):
    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        b = array_api.copy(a)
        b[b < 0] = 0
        return b
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        a = node.inputs[0].realize_cached_data()
        a[a < 0] = 0
        a[a > 0] = 1
        return out_grad * a
        ### END YOUR SOLUTION

def relu(a):
    return ReLU()(a)

